/* 
Secuencias de Escape

Codigo | Resultado
------------------
\'      Comilla Simple
\"      Comilla Doble
\\      Barra invertida
\n      Linea Nueva
\r      Retorno de Carro
\t      Tabulacion
\b      Retroceso
\f      Saldo de Pagina

*/

console.log("Estoy aprendiendo \"JavaScript\"");  //Comilla Doble
//* R.  Estoy aprendiendo "JavaScript"

console.log("Estoy aprendiendo \\ JavaScript"); // Barra Invertida
//* R.  Estoy aprendiendo \ JavaScript

console.log("Estoy aprendiendo: \n JavaScript"); // Linea Nueva
//* R. Estoy aprendiendo:
//*     JavaScript